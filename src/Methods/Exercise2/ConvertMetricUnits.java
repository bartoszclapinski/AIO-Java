package Methods.Exercise2;

public class ConvertMetricUnits {

    double convertMetersToCentimeters(double meters) {
        return meters * 100;
    }

    double convertMetersToMillimeters(double meters) {
        return meters * 1000;
    }

    double convertCentimetersToMeters(double centimeters) {
        return centimeters / 100;
    }

    double convertMillimetersToMeters(double millimeters) {
        return millimeters / 1000;
    }
}
