package Methods;

public class Calc {

    int add(int a, int b) {
        return a + b;
    }

    int sub(int a, int b) {
        return a - b;
    }

    int mul(int a, int b) {
        return a * b;
    }

    int div(int a, int b) {
        return a / b;
    }

    void addAndPrint(int a, int b) {
        System.out.println(a + b);
    }
}
